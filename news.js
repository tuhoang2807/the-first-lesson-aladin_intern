const itemsPerPage = 8; // 4 dòng, 2 sản phẩm mỗi dòng
let currentPage = 1;

function displayNewsItems(page) {
    const newsContainer = document.getElementById('news-container');
    newsContainer.innerHTML = '';

    const start = (page - 1) * itemsPerPage;
    const end = start + itemsPerPage;
    const paginatedNews = newsItems.slice(start, end);

    paginatedNews.forEach(item => {
        const newsItemDiv = document.createElement('div');
        newsItemDiv.classList.add('item');

        const img = document.createElement('img');
        img.src = item.imgSrc;
        img.alt = item.title;

        const textContainer = document.createElement('div');

        const h4 = document.createElement('h4');
        h4.textContent = item.title;

        const p = document.createElement('p');
        p.textContent = item.description;

        textContainer.appendChild(h4);
        textContainer.appendChild(p);

        newsItemDiv.appendChild(img);
        newsItemDiv.appendChild(textContainer);

        newsContainer.appendChild(newsItemDiv);
    });
}

